let quit = false;


while (!quit) {
    let input = prompt("What would you like to get done today?");
    input = input.toLowerCase();
    // initial input must be 'new' 'list' 'quit' 'delete' or 'quit'

    switch (input)
    {
        case 'new':
            afdsa
        case 'list':
            SVGFEDropShadowElement
        case 'delete':
            cdsafdsa
        case 'quit':
            quit = true;
        default:
            console.log("Invalid input");
    }
}